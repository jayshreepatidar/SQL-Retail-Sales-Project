-- 5. BUSINESS ANALYSIS
-- Basic Queries
-- 1.	Retrieve all sales records from the dataset.
SELECT * FROM retail_sales;


-- 2.	Show all transactions where the category is Clothing.
SELECT * FROM retail_sales
WHERE category = 'Clothing';


-- 3.	Find all sales made on 2022-11-05.
SELECT * FROM retail_sales
WHERE sale_date = '2022-11-05';


-- 4.	Retrieve transactions where total_sale > 1000.
SELECT * FROM retail_sales
